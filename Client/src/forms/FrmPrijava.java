    private void btnPosaljiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPosaljiActionPerformed
        try {
            if (txtDatumVolontiranja.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Morate uneti datum!");
                return;
            }

            SimpleDateFormat sdf = new SimpleDateFormat("dd.MM.yyyy");
            sdf.setLenient(false);
            Date datumVol = sdf.parse(txtDatumVolontiranja.getText());

            Date danas = new Date();
            Date finale = sdf.parse("01.02.2026");

            if (datumVol.before(danas)) {
                JOptionPane.showMessageDialog(this, "Datum volontiranja mora biti u budućnosti!");
                return;
            }
            if (datumVol.after(finale)) {
                JOptionPane.showMessageDialog(this, "Turnir se završava 01.02.2026!");
                return;
            }

            Prijava p;

            if (this.prijavaZaIzmenu != null) {
                p = this.prijavaZaIzmenu;
            } else {
                p = new Prijava();
                p.setId(UUID.randomUUID().toString());
                p.setKorisnik(ulogovani);
                p.setDatumPrijave(new Date());
                p.setStatus("Na čekanju");
            }

            p.setDatumVolontiranja(datumVol);
            p.setSmena((Smena) cmbSmena.getSelectedItem());
            p.setPozicija((Pozicija) cmbPozicija.getSelectedItem());

            if (this.prijavaZaIzmenu != null) {
                communication.Communication.getInstance().editPrijava(p);
                JOptionPane.showMessageDialog(this, "Prijava uspešno izmenjena!");
                this.dispose();
            } else {
                communication.Communication.getInstance().addPrijava(p);

                kreirajPotvrduFajl(p);
                JOptionPane.showMessageDialog(this, "Uspešno ste se prijavili!");
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Greška: " + e.getMessage());
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnPosaljiActionPerformed

