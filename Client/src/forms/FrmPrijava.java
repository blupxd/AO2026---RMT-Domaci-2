    private void kreirajPotvrduFajl(Prijava p) {
        try {
            String imeFajla = "potvrda_" + p.getKorisnik().getJmbg() + "_" + p.getId().substring(0, 5) + ".txt";

            FileWriter fw = new FileWriter(imeFajla);
            try (PrintWriter pw = new PrintWriter(fw)) {
                pw.println("=== POTVRDA PRIJAVE ZA VOLONTIRANJE ===");
                pw.println("Australian Open 2026");
                pw.println("=======================================");
                pw.println("Volonter: " + p.getKorisnik().getIme() + " " + p.getKorisnik().getPrezime());
                pw.println("JMBG: " + p.getKorisnik().getJmbg());
                pw.println("Email: " + p.getKorisnik().getEmail());
                pw.println("---------------------------------------");
                pw.println("Datum volontiranja: " + new SimpleDateFormat("dd.MM.yyyy").format(p.getDatumVolontiranja()));
                pw.println("Smena: " + p.getSmena());
                pw.println("Pozicija: " + p.getPozicija());
                pw.println("Datum slanja prijave: " + new SimpleDateFormat("dd.MM.yyyy HH:mm").format(p.getDatumPrijave()));
                pw.println("=======================================");
            }

        } catch (IOException ex) {
            System.out.println("Greška pri kreiranju fajla: " + ex.getMessage());
        }
    }

